public class ForLoopConcept {
    public static void main(String[] args) {
        
        System.out.println("=== Printing 1 to 100 Using smart For Loop ===");
        
        // Smart approach: Initialization, Condition, and Updation all in one line!
        for (int i = 1; i <= 100; i++) {
            System.out.print(i + " ");
        }
        
        System.out.println("\n\nLoop completed successfully!");
    }
}
