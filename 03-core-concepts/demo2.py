# File: demo2.py -> Example of Dynamically Typed Language (Python)

# No need to declare any data type (int, String, etc.)
a = 10
print(a)

# We can easily check what type the variable became at Runtime
print(type(a))  # Output: <class 'int'>

# Same variable container "a" is now storing a String!
# (Like the water bottle analogy from the video)
a = "Vinay"
print(a)
print(type(a))  # Output: <class 'str'>
